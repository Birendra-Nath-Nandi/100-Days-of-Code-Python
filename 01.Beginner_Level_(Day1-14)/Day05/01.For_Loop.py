# For Loop

# for loop with lists
fruits = ["apple", "banana", "orange"]

for fruit in fruits:
    print(fruit)
    print(fruit + " pie")

# for loop with Range function
for number in range(1, 11):
    print(number) # prints 1 to 10

# Sum 1 to 100
total = 0
for number in range(1, 101):
    total += number
print(total)
