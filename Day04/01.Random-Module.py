# Random Module - is a powerful tool for generating random numbers.
import random

# Generates Random integer (in this case 1 to 10)
random_integer = random.randint(1, 10)
print(random_integer)

# Generates Random integer (in this case 1 to 10)
random_integer = random.randint(1, 9999)
