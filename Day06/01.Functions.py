# Creating a Function

# A function is a block of code that performs a specific task. In Python, functions are defined using the def keyword.

# The syntax for defining a function is as follows:
# def function_name(parameters):
#     function_body

def my_function():
    print("Hello from a function")

# Calling a Function
# To call a function, use the function name followed by parenthesis

my_function()

